package com.continuuity.app.deploy;

import java.io.InputStream;
import java.util.concurrent.Callable;

/**
 * This interface is used for defining the execution of configure either in sandbox jvm for distributed mode
 * or within a thread in a single node.
 *
 * <p>
 *  This interface extends from {@link Callable} with the intent that the callee is responsible for making
 *  sure that he runs this in a thread that allows to timeout the execution of configure.
 * </p>
 */
public interface Configurator extends Callable<Configurator.Response> {

  /**
   * Invoked to kill the process of configuration.
   * <p>
   *   In sandbox mode, if the execution of application process is taking more than expected, then
   *   invoking this function would kill the process.
   * </p>
   */
  void destory();

  /**
   * Interface defining the response as returned by the execution of configure method.
   */
  public static interface Response {
    /**˜                                            ˜
     * @return {@link InputStream} of configuration data as generated by invoking configure.
     */
    public InputStream get();
  }
}
